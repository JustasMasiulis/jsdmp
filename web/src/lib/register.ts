const REGISTER_NAMES: string[] = [];

function fill(
	start: number,
	prefix: string,
	count: number,
	suffix = "",
	from = 0,
) {
	for (let i = 0; i < count; i++)
		REGISTER_NAMES[start + i] = `${prefix}${from + i}${suffix}`;
}

function named(entries: [number, string][]) {
	for (const [idx, name] of entries) REGISTER_NAMES[idx] = name;
}

